fun main() {
    val scan = java.util.Scanner(System.`in`)
    var n = scan.nextInt()
    var arr = IntArray(n)
    for (i in 0 until n) {
        arr[i] = scan.nextInt();
    }
    for (i in 0 until n) {
        if (arr[i] % 3 == 0 && arr[i] != 0) {
            println(arr[i])
        }
    }
}
